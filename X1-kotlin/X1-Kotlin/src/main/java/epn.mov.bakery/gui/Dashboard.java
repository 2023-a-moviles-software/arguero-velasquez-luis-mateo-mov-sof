    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name= jTFNewBreadName.getText();
        Double price = (Double) jSNewBreadPrice.getValue();
        Boolean isSweet = jChNewBreaIsSweet.isSelected();
        LocalDate elaborationDate = jTFNewBreadEDate.getText().transform(LocalDate::parse);
        Integer stock = (Integer)jSNewBreadUnits.getValue();

        Bread b = new Bread(name,price,elaborationDate,isSweet,stock);

        bakery.bakeBread(b);
        updateList();
        resetNewBread();
        SingletonManager.Companion.setBakery(bakery);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Integer breadsToSell = (Integer) jSSellBreadCount.getValue();
        boolean enoughBread = bakery.sellBread(chosenBreadName,breadsToSell);
        if(!enoughBread){
            JOptionPane.showMessageDialog(
                    this,
                    "No hay suficiente pan para completar esta transaccion",
                    "No hay pan",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        bakery.discardOutOfStock();
        updateList();
        SingletonManager.Companion.setBakery(bakery);
        JOptionPane.showMessageDialog(
                this,
                "Venta exitosa",
                "Exito",
                JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton3ActionPerformed

