    private void jBDiscardActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBDiscardActionPerformed
        Integer maxAge = (Integer) jSMaxAge.getValue();
        Boolean affectAll = jCBDiscardAll.isSelected();
        Integer oldCount = bakery.getBreadsCount().values().stream().mapToInt(t->t).sum();

        if(affectAll) bakery.discardBreadOlderThan(maxAge);
        else bakery.getByName(chosenBreadName).removeIf(bread -> bakery.isBreadExpired(bread,maxAge));
        Integer newCount = bakery.getBreadsCount().values().stream().mapToInt(t->t).sum();

        JOptionPane.showMessageDialog(
                this,
                "Se han eliminado "+(oldCount-newCount)+" panes del stock",
                "Exito",
                JOptionPane.INFORMATION_MESSAGE);

        updateList();
        updateTable();
        SingletonManager.Companion.setBakery(bakery);
    }//GEN-LAST:event_jBDiscardActionPerformed

