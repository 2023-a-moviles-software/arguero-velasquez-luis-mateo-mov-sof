    public void updateList(){
        DefaultListModel<String> listModel = new DefaultListModel<>();

        displayNameToName = bakery.getBreadsCount()
                .entrySet()
                .stream()
                .map(e->{
                    String displayName = e.getKey()+"("+e.getValue()+")";
                    String name = e.getKey();
                    return Map.entry(displayName,name);
                }).collect(Collectors.toMap(e->e.getKey(),e->e.getValue()));

        listModel.addAll(displayNameToName.keySet());

        jList1.setModel(listModel);
    }

    public void updateTable(){
        DefaultTableModel tm = new DefaultTableModel();
        tm.addColumn("Nombre");
        tm.addColumn("Precio");
        tm.addColumn("Fecha Elab.");
        tm.addColumn("Es dulce");
        tm.addColumn("Stock");
        bakery.getByName(chosenBreadName)
                .stream()
                .map(bread->new String[]{
                        bread.getName(),
                        String.valueOf(bread.getPrice()),
                        bread.getElaborationDate().toString(),
                        String.valueOf(bread.isSweet()),
                        String.valueOf(bread.getStock())
                })
                .forEach(tm::addRow);
        jTable1.setModel(tm);
    }

